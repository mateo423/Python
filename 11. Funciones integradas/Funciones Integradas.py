#Funciones Integradas 

n = bin(245)   #Convertir el valor 10 en binario
print(n)

a = hex(10)    #Convertir el valor 10 en hexagonal
print(a)

m = int("0b1010",2)   #Convertir un codigo binario en un entero
print(m)           

b = abs(-5)       #Es el valor absoluto de numero
print(b)

v = round(5.6)     # Esta variables redondea este numero
print(v)

l = len("Mateo")   # Cuantos caracteres tiene mi nombre o letra 
print(l)